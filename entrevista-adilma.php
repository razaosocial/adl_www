    <p>Sexta-feira, 28 de novembro de 2008<br />
    <b style="font-size: 14px">Adilma de Sousa Oliveira<br />
    Avisa Lá</b><br />
<br />
    Adilma de Sousa Oliveira é representante da EMEF Serafina Carvalho, em Itupiranga – PA, vencedora do Prêmio Victor Civita Professor Nota 10 na categoria Escola, com o projeto Formação de Formadores: construindo competência a caminho da profissionalização permanente no espaço escolar.
    Formação de professores para uma Educação de qualidade.<br /><br />
    <b>1. Explique o projeto vencedor do Prêmio Victor Civita Professor Nota 10, na categoria Prêmio Escola?</b><br />
    O projeto focou a formação de professores dentro da escola. O objetivo foi desenvolver a leitura e a escrita. Nossa meta era ter alunos e professores produtores e leitores de textos. O primeiro passo foi formar o educador para que depois ele qualificasse sua turma. Todo o embasamento teórico veio do Programa Além das Letras, que atuou em parceria com a Secretaria Municipal de Educação.<br />
<br />
    <b>2. Um processo de formação não é fácil. Quais foram as dificuldades enfrentadas durante o trabalho?</b><br />
    No início, tivemos um grande obstáculo: a falta de comprometimento por parte dos docentes. Com o tempo, foi acontecendo um processo de conquista e, finalmente, essa dificuldade foi vencida. Hoje, ainda temos alguns problemas, como a falta de equipamento de vídeo para filmar as atividades em sala para posterior avaliação do grupo. Reunir os educadores também é complicado, mas contornável. E, por fim, sentimos que há pouco tempo para o atendimento individual com cada membro da equipe de professores.<br /><br />
    <b>3. Mesmo sendo um projeto premiado, é possível apontar o que precisa ser melhorado?</b><br />
    Sim, há coisas a serem melhoradas. O número de turmas atendidas é uma delas. Se tivéssemos menos atendimentos, o trabalho teria mais qualidade. A avaliação é outro ponto. Usamos diversos instrumentos avaliativos e poderíamos experimentar outros para melhorar.<br />
 <br />
    <b>4. O que os alunos ganharam com o projeto?</b><br />
    Em nossa comunidade, a maioria dos pais é analfabeta funcional. As crianças que atendemos têm contato com livros apenas quando estão na escola. A responsabilidade é totalmente nossa. Antes do iniciar o projeto, nossos estudantes tinham dificuldades para produzir textos, escreviam um amontoado de palavras e frases e isso foi superado. Se sentimos que algo não vai bem em sala de aula, detectamos com o diagnóstico, que é realizado mensalmente. Depois, planejamos pautas para sanar as questões. Atualmente, eles gostam de ler, freqüentam mais a sala de leitura e escrevem seus contos e suas poesias. Também reconhecem um gênero textual e conseguem fazer revisão dos seus textos escritos.<br /><br />
    <b>5. Qual a relação do projeto premiado com o Programa Além das Letras?</b><br />
    Foi com Programa que fizemos o curso de formação de formadores e aprendemos a construir pautas e analisar as avaliações. Todos os conteúdos que realizamos com os professores foram aprendidos na formação. Eu diria que o prêmio não é só meu. É também do Além das Letras e da equipe de formação representada pela Léiva e Katilvânia (Léiva Rodrigues de Sousa e Katilvânia de Sousa Guedes atuaram como formadoras da equipe escolar). Ele é nosso!
