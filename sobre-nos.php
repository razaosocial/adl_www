<h1>Sobre Nós</h1>

<h2>Programa Além das Letras</h2>
<div class="align_center">
<p>O Programa Além das Letras nasceu do sonho da equipe do Instituto Avisa Lá
     de que é possível alfabetizar crianças de baixa renda para que, ao completarem
     o Ensino Fundamental, além de saber ler e escrever efetivamente, elas sejam capazes de
     entender e atribuir novos significados ao que lêem e consigam se comunicar plenamente
     por meio da escrita. A primeira ação do programa é identificar os municípios que realizam
     um bom trabalho. Desde 2004 o Além das Letras vêm selecionando e divulgando boas práticas 
     de formação em alfabetização desenvolvidas pelas equipes técnicas das secretarias municipais 
     de educação de cinco regiões do País. Com este grupo de municípios foi criada a Comunidade Além das Letras.
     </p>
     <p>Essa comunidade virtual busca estimular o aperfeiçoamento de educadores, a troca 
      de experiências bem-sucedidas e a construção de conhecimento na área. Isso é feito em
      parceria com as equipes técnicas dos municípios participantes, por meio do desenvolvimento 
      de projetos de formação continuada para coordenadores pedagógicos e professores. Tais experiências 
      devem contemplar um diagnóstico local e um plano de ação visando melhorias na qualidade das práticas 
      de leitura e de escrita dos alunos das séries iniciais do Ensino Fundamental. O fortalecimento e ampliação 
      das iniciativas locais pretendem influenciar na alfabetização das crianças, garantindo, no âmbito municipal,
      a utilização de princípios mais eficazes de formação continuada por meio de atuação com os técnicos formadores,
      coordenadores pedagógicos e professores das redes públicas de educação.
     </p>
<h2>Quem faz a rede</h2>
<p>
    Uma união de diferentes parceiros foi constituída para realizar o Programa Além das Letras: o Instituto Avisa Lá (IAL),
    responsável técnico e idealizador do Programa; a Fundação Avina, apoiador inicial; o Grupo Gerdau, patrocinador e a IBM,
    parceira tecnológica. Além disso, o suporte ao planejamento, à gestão, à busca de parceiros e a coordenação tecnológica 
    são de responsabilidade do Instituto Razão Social. A Ashoka, a Undime, a Unesco e o Unicef são parceiros institucionais.
</p>
<p>A Comunidade Além das Letras conta com uma equipe constituída por profissionais dos Institutos Avisa Lá e Razão Social. </p>
