<div class="alinhamento_adn">
<p>
    O Além dos Números nasceu a partir dos bons resultados do Programa de formação à distância na área de 
    alfabetização estruturado pelos mesmos parceiros – o Além das Letras. O propósito do Programa Além dos 
    Números é incentivar mudanças positivas nos indicadores de rendimento escolar em Matemática e apoiar o 
    sucesso escolar dos alunos do Ensino Fundamental do 1º ao 5º ano.
</p>
<p>
    A proposta é que o professor organize atividades em que o aluno decida sobre os conhecimentos que utilizará 
    para resolver situações problema de diferentes âmbitos da matemática, discuta suas ideias com os colegas,
    reflita sobre sua produção, relacione conhecimentos anteriores com a nova situação, formule conjeturas e produza 
    argumentos para validá-las.
</p>
<p>
    O Programa se viabiliza por meio de uma formação em rede, na qual os técnicos 
    das Secretarias de Educação de diversos municípios brasileiros são os sujeitos da formação e
    reeditores reflexivos das propostas em suas redes locais.<br />
    Os técnicos das Secretarias realizam a formação de coordenadores pedagógicos que por sua vez capacitam seus professores, criando uma cultura de formação permanente no município. 
</p>
