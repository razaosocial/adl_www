<h1>O papel profissional do coordenador pedagógico</h1>
<p>
     O objetivo do Além das Letras é investir na formação das equipes técnicas das Secretarias
     de Educação para que esses formadores locais invistam, por sua vez, nos coordenadores pedagógicos 
     das unidades escolares, contribuindo para a constituição de espaços de formação permanentes. O foco maior
     do Programa Além das Letras é, portanto, o coordenador pedagógico. Nós temos o desafio de ajudá-lo a 
     redimensionar sua função na escola para que compreenda seu papel na coordenação de um projeto de formação 
     com conteúdos claros e consistentes, visando a formação de seus professores. Para muitos municípios participantes
     da rede, essa proposta de formação permanente é nova. Ela vai exigir uma reorganização da Secretaria de Educação
     quanto aos papéis destinados aos formadores locais. Não é uma tarefa fácil e temos clareza que exigirá, em muitos 
     casos, um tempo de reflexão da equipe e negociações internas para efetivar mudanças necessárias.
</p>
<p>Sabemos que em alguns lugares existe uma cultura escolar em que o coordenador 
assume muitas atribuições administrativas, pois recebe uma demanda enorme de tarefas. 
Não sobra tempo para se dedicar à formação dos professores e, muitas vezes, o coordenador 
não é reconhecido como um formador.
</p>
<p>
    Por este e por outros motivos, há muitas situações em que, 
    para cumprir minimamente sua responsabilidade junto aos professores, 
    os coordenadores adotam uma linha mais prescritiva, dizendo aos professores
    o que eles precisam fazer, entregando o planejamento pronto, explicando demandas,
    mas sem incorporar na sua ação os saberes dos professores. Sem dúvida, esta situação
    torna o professor muito dependente, sem autoria. Isto evidentemente não o ajuda a se tornar
    um profissional pesquisador, reflexivo e autônomo, qualidades exigidas de um educador contemporâneo.
