<h1>Conteúdos que vão Além das Letras</h1>
<div class="align_center">
<p>
Ensinar a ler e escrever - missão original e irrenunciável da escola – significa desenvolver práticas sociais que envolvam a escrita. Nosso objetivo final é contribuir para a formação de usuários competentes da língua escrita. Não é mais possível pensar na alfabetização somente como um processo de apropriação de um código. Hoje já se sabe que aprender a ler e escrever envolve dois processos:
</p>
 <ul>
    <li>compreender a natureza do sistema alfabético de escrita – as relações entre som-grafia, a segmentação entre as palavras, as restrições ortográficas;</li>
    <li>compreender o funcionamento da linguagem escrita – suas características específicas, suas diferentes formas e gêneros</li>
 </ul>
 <p>Esses dois processos devem ser trabalhados de forma concomitante na escola. Isso quer
  dizer que não basta colocar os alunos diante de textos para que aprendam o funcionamento do 
  sistema alfabético e também que a aquisição do sistema alfabético por si só não garante a 
  possibilidade de participar com sucesso das práticas sociais de leitura, escrita e comunicação oral. 
  Reproduzir as condições que contextualizam a leitura e a escrita fora da escola dentro do âmbito escolar e 
  planejar situações de aprendizagem específicas para que os alunos aprendam o sistema alfabético de escrita 
  representam o nosso maior desafio.
  </p>
  <h2>Um recorte didático</h2>
  <p>Para preservar a natureza da leitura e da escrita como práticas sociais, o Programa Além das Letras selecionou 
     como conteúdos principais da formação algumas práticas fundamentais para o processo de alfabetização nas séries 
     iniciais. Uma alfabetização que tenha como objetivo formar leitores e escritores plenos (ou seja, participantes da cultura escrita) 
     pressupõe um trabalho didático que contemple:
  </p>
  <ul>
    <li>Leitura feita pelo professor, situação didática que assegura ao aluno o acesso à diversidade de textos mesmo antes de lerem convencionalmente;</li>
    <li>Produção de textos, trabalho planejado para que os alunos produzam textos de diferentes tipos mesmo antes de escreverem convencionalmente, façam revisões coletivamente e tenham a oportunidade de discutir e analisar as características discursivas de um texto;</li>
    <li>Situações de escrita e de leitura pela criança, que contextualizam a reflexão sobre o funcionamento do sistema alfabético de escrita e garantem aos alunos a oportunidade de pensar diariamente sobre como se faz para ler e escrever.</li>
  </ul>
  <p>Em um processo de alfabetização a criança passa por todas essas situações simultaneamente. No entanto, 
     para efeito da apresentação didática desse conteúdo aos professores, o programa Além das Letras enfoca tais 
     situações em três módulos: os municípios devem escolher um deles (o que não significa que se deixe de lado um ou outro conteúdo) 
     para estudar com mais profundidade, receber materiais de apoio e supervisão para o desenvolvimento de projetos de formação que 
     abordem tais conteúdos.
  </p>
  <p>
      Os municípios que entram no programa agora ou os que já participam há mais tempo devem escolher o 
      projeto que desejam desenvolver. Essa escolha deve ser pensada em função das necessidades que seu município 
      apresenta no momento, a partir de uma avaliação da aprendizagem dos alunos, do que sabem, do que não sabem e do 
      que ainda podem aprender com a ajuda de seu professor. Veja a seguir como tais conteúdos se apresentam.
