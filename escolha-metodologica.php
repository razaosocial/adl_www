<h1>Uma escolha metodológica</h1>
<p>
    Um coordenador que saiba muito acerca dos conhecimentos didáticos
    necessários para alfabetizar é importante, mas não é condição suficiente
    para ser um bom formador na rede. Pode-se saber muito sobre algo, mas saber 
    pouco sobre como os professores aprendem. Por isso, nossa metodologia consiste 
    em construir situações nas quais os coordenadores possam, por meio do apoio da 
    equipe técnica de cada município participante da rede Além das Letras, planejar 
    os seus encontros, considerando o que sabem os professores das suas unidades. Além
    de avançar no conhecimento didático, o coordenador também aprenderá a tomar decisões
    em relação às estratégias de formação mais adequadas tais como:
</p>
<table>
	<td>	
        <ul>
            <li>Observação de aula</li>
